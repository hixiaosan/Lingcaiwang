package com.bgf.lingcaiwang.bean;

/**
 * 新闻
 */
public class News {

    private String id;

    private String title;

    private String art_writer;

    private String art_time;

    private String art_img;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getArt_writer() {
        return art_writer;
    }

    public void setArt_writer(String art_writer) {
        this.art_writer = art_writer;
    }

    public String getArt_time() {
        return art_time;
    }

    public void setArt_time(String art_time) {
        this.art_time = art_time;
    }

    public String getArt_img() {
        return art_img;
    }

    public void setArt_img(String art_img) {
        this.art_img = art_img;
    }
}
